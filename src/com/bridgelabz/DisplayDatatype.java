package com.bridgelabz;

public class DisplayDatatype {
	public static void main(String[] args) {
		int num1=10;
	    char num2='a';
	    float num3=10.81f;
	    long num4=123444;
	    double num5=12233.111;
	    byte num6=12;
	    short num7=1234;
	    boolean num8=true;
		System.out.println("int datatype is :"+ num1);
		System.out.println("char datatype is:" +num2);
		System.out.println("float datatype is:" +num3);
		System.out.println("double datatype is" +num4);
		System.out.println("long datatype is" +num5);
		System.out.println("byte datatype is" +num6);
		System.out.println("short datatype is" +num7);
		System.out.println("boolean datatype is" +num8);

	}
}


